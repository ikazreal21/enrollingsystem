    public void id(){
        
        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select MAX(eid) from enroll_list");
            rs.next();
            rs.getString("MAX(eid)");
            
            if(rs.getString("MAX(eid)")==null){
                jLabel3.setText("EN01");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(eid)").substring(2, rs.getString("MAX(eid)").length()));
                id++;
                jLabel3.setText("EN" + String.format("%03d", id));
            }
           
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(studentenrollform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

