    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String eid = jLabel3.getText();
        String ename = name.getText();
        String eage = age.getText();
        String gender = sex.getSelectedItem().toString();
        SimpleDateFormat bdate = new SimpleDateFormat("yyyy-MM-dd");
        String bday = bdate.format(birthdate.getDate());
        String eciti = citizenship.getText();
        String eaddre = address.getText();
        String ereligion = religion.getText();
        String grades = grade.getSelectedItem().toString();
        String ephone = phone.getText();

        
        try {
            pst = conn.prepareStatement("insert into enroll_list(eid, ename, age, sex, birthday, citizenship, address, religion, grade, phone) values(?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, eid);
            pst.setString(2, ename);
            pst.setString(3, eage);
            pst.setString(4, gender);
            pst.setString(5, bday);
            pst.setString(6, eciti);
            pst.setString(7, eaddre);
            pst.setString(8, ereligion);
            pst.setString(9, grades);
            pst.setString(10, ephone);
            pst.executeUpdate();
            
            int res3 = JOptionPane.showOptionDialog(new JFrame(), "Do you Confirm to Enroll of " + ename + "\nin the grade of " + grades,"Confirmation",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[] { "Yes", "No" },
                    JOptionPane.YES_OPTION);
                    
            switch (res3) {
                case JOptionPane.YES_OPTION:
                    JOptionPane.showMessageDialog(this, "Enrollment Succesfull");
                    name.setText("");
                    phone.setText("");
                    address.setText("");
                    religion.setText("");
                    citizenship.setText("");
                    age.setText("");
                    sex.setSelectedIndex(-1);
                    grade.setSelectedIndex(-1);
                    id();
                    break;
                case JOptionPane.NO_OPTION:
                    System.exit(0);
                default:
                    break;
            }
            
           
            
        } catch (SQLException ex) {
            Logger.getLogger(studentenrollform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

